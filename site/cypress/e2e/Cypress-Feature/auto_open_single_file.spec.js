describe('Test cases involving auto opening single file submissions', () => {
    function assertSubmissionsBrowserClosed() {
        cy.get('#div_viewer_sd1').should('not.be.visible');
    }

    function assertSubmissionsBrowserOpen() {
        // have to increase timeout so that the file can be loaded properly in the CI
        cy.get('#div_viewer_sd1', { timeout: 20000 }).should('be.visible');
    }

    function assertResultsBrowserClosed() {
        cy.get('#div_viewer_rd1').should('not.be.visible');
    }

    function assertResultsBrowserOpen() {
        // we can't check that it is visible because in the CI it's possible
        // that autograding hasn't finished yet
        cy.get('#div_viewer_rd1', { timeout: 20000 }).should('exist');
    }

    function assertAllFoldersClosed() {
        cy.get('[id^=div_viewer_]').should('not.be.visible');
    }

    function assertAllFilesClosed() {
        cy.get('[id^=file_viewer_]').should('not.be.visible');
    }

    const single_file_submission_selector = '#div_viewer_sd1 div[id^=file_viewer_]:not([data-file_name^="."])';

    function assertSingleFileSubmissionOpen() {
        // a single file submission should only include one file
        // (excluding autogenerated files that start with ".")
        cy.get(single_file_submission_selector).should('length', 1);
        cy.get(single_file_submission_selector).should('be.visible');
    }

    function assertSingleFileSubmissionClosed() {
        cy.get(single_file_submission_selector).should('length', 1);
        cy.get(single_file_submission_selector).should('not.be.visible');
    }

    const single_file_submission_clickable_selector = '#div_viewer_sd1 .openable-element-submissions:not([data-file_name^="."])';

    function clickSingleFileSubmission() {
        cy.get(single_file_submission_clickable_selector).should('length', 1);
        cy.get(single_file_submission_clickable_selector).click();
    }

    it('test the single file auto open for text submissions', () => {
        cy.visit(['sample', 'gradeable', 'grading_homework', 'grading', 'details']);
        cy.login('instructor');
        cy.get('[data-testid="view-sections"]').uncheck();
        cy.get('#details-table').contains('rennea').siblings().eq(6).click();
        cy.get('#submission_browser_btn').click();

        cy.get('#autoscroll_id').click();
        cy.get('#autoscroll_id').should('be.checked');

        // single file submission is opened if nothing else is open
        assertAllFoldersClosed();
        assertAllFilesClosed();
        assertSingleFileSubmissionClosed();
        cy.reload();
        assertSubmissionsBrowserOpen();
        assertSingleFileSubmissionOpen();

        // closing the single file submission disables single file auto open
        clickSingleFileSubmission(); // close the submitted file
        assertSubmissionsBrowserOpen();
        cy.reload();
        assertSubmissionsBrowserOpen();
        assertAllFilesClosed();

        cy.get('#submissions').click(); // close submissions browser
        assertSubmissionsBrowserClosed();
        cy.reload();
        assertSubmissionsBrowserOpen();
        assertSingleFileSubmissionOpen();

        // opening any other folder or file disables single file auto open
        assertResultsBrowserClosed();
        cy.get('#results').click(); // open results browser
        assertResultsBrowserOpen();
        assertSubmissionsBrowserOpen();
        assertSingleFileSubmissionOpen();

        clickSingleFileSubmission(); // close the submitted file
        cy.reload();
        assertResultsBrowserOpen();
        assertSubmissionsBrowserOpen();
        assertAllFilesClosed();

        cy.get('#results').click(); // close results browser
        assertResultsBrowserClosed();
        cy.reload();
        assertResultsBrowserClosed();
        assertSubmissionsBrowserOpen();
        assertAllFilesClosed();

        cy.get('#submissions').click(); // close submissions browser
        assertSubmissionsBrowserClosed();
        cy.reload();
        assertSubmissionsBrowserOpen();
        assertSingleFileSubmissionOpen();

        // switching between single file submission with different names works with auto open
        cy.get('#next-student').click();
        assertSubmissionsBrowserOpen();
        assertSingleFileSubmissionOpen();

        clickSingleFileSubmission(); // close the submitted file
        cy.get('#submissions').click(); // close submissions browser
        cy.get('#prev-student').click();
        assertSubmissionsBrowserOpen();
        assertSingleFileSubmissionOpen();

        // opening a submission with multiple files defaults to normal behavior
    });

    /*
        FIX ME! determining whether an image is open or closed in the grading panel requires some
        finnicky CSS because Cypress's (not.)be.visible gets tripped up by the #file-view's "hide" class
        combined with the use of display inline styling. A better approach to toggling the file view
        would be to toggle only classes or only inline styles, not both.
    */
    const file_view_selector = '#file-view[style^="display: block;"]';

    function assertSingleImageSubmissionClosed() {
        cy.get(file_view_selector).should('not.exist');
    }

    function assertSingleImageSubmissionOpen() {
        cy.get(file_view_selector).should('length', 1);
    }

    function clickFileViewCollapseButton() {
        cy.get('a:has(i[title="Back"])').click();
    }

    it('test the single file auto open for image submissions', () => {
        cy.visit(['sample', 'gradeable', 'grading_homework_pdf', 'grading', 'details']);
        cy.login('instructor');
        cy.get('[data-testid="view-sections"]').uncheck();
        cy.get('#details-table').contains('beahaf').siblings().eq(6).click();
        cy.get('#submission_browser_btn').click();

        cy.get('#autoscroll_id').click();
        cy.get('#autoscroll_id').should('be.checked');

        // single image file submission is opened and fills the screen if nothing else is open
        assertAllFoldersClosed();
        assertAllFilesClosed();
        assertSingleImageSubmissionClosed();
        cy.reload();
        assertSubmissionsBrowserClosed();
        assertSingleImageSubmissionOpen();

        // switching between single image file submission with different names works with auto open
        cy.get('#next-student').click();
        assertSingleImageSubmissionOpen();

        clickFileViewCollapseButton();
        assertSingleImageSubmissionClosed();

        cy.get('#prev-student').click();
        assertSingleImageSubmissionOpen();
    });

    it('test that multi-file submissions are unaffected', () => {
        cy.visit(['sample', 'gradeable', 'grading_pdf_peer_homework', 'grading', 'details']);
        cy.login('instructor');
        cy.get('[data-testid="view-sections"]').uncheck();
        cy.get('#details-table').contains('adamsg').siblings().eq(6).click();
        cy.get('#submission_browser_btn').click();

        cy.get('#autoscroll_id').click();
        cy.get('#autoscroll_id').should('be.checked');

        // opening a multi-file submission does not sure auto open single file logic
        cy.reload();
        assertAllFilesClosed();
        assertAllFoldersClosed();
    });
});
